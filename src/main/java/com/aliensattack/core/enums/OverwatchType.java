package com.aliensattack.core.enums;

/**
 * Defines different types of overwatch abilities
 */
public enum OverwatchType {
    NORMAL,         // Standard overwatch
    AMBUSH,         // Coordinated overwatch with multiple units
    REACTION,       // Reactive overwatch with bonus accuracy
    SUPPRESSION,    // Suppressive overwatch fire
    COVERING        // Covering fire overwatch
} 