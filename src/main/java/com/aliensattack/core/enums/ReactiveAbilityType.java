package com.aliensattack.core.enums;

/**
 * Defines different types of reactive abilities
 * Enhanced with comprehensive XCOM 2 reactive ability types
 */
public enum ReactiveAbilityType {
    // Original reactive abilities
    BLADESTORM,     // Reactive melee attacks when enemies move nearby
    LIGHTNING_REFLEXES, // Reactive dodge abilities
    RETURN_FIRE,    // Reactive shooting when attacked
    COUNTER_ATTACK, // Reactive melee counter-attacks
    REACTIVE_ARMOR, // Reactive armor abilities
    
    // Enhanced XCOM 2 reactive abilities
    OVERWATCH_AMBUSH,    // Reactive overwatch ambush attacks
    HEIGHT_ADVANTAGE,    // Reactive height advantage attacks
    FLANKING_BONUS,      // Reactive flanking bonus attacks
    SUPPRESSION_RESPONSE, // Reactive suppression response
    PSIONIC_SHIELD,      // Reactive psionic shield
    HACKING_COUNTER,     // Reactive hacking counter
    EXPLOSIVE_REACTION,  // Reactive explosive reactions
    CHAIN_REACTION,      // Reactive chain reactions
    DEEP_COVER_RESPONSE, // Reactive deep cover response
    SQUAD_SIGHT_SNIPE,   // Reactive squad sight snipe
    BLUESCREEN_COUNTER,  // Reactive bluescreen counter
    VOLATILE_MIX_REACTION, // Reactive volatile mix reaction
    RAPID_FIRE_RESPONSE  // Reactive rapid fire response
} 