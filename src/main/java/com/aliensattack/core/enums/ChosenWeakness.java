package com.aliensattack.core.enums;

/**
 * Defines different types of Chosen weaknesses in XCOM 2
 */
public enum ChosenWeakness {
    FIRE_VULNERABILITY,      // Vulnerable to fire damage
    ICE_VULNERABILITY,       // Vulnerable to ice damage
    ELECTRIC_VULNERABILITY,  // Vulnerable to electric damage
    POISON_VULNERABILITY,    // Vulnerable to poison damage
    PSYCHIC_VULNERABILITY,   // Vulnerable to psychic attacks
    PHYSICAL_VULNERABILITY,  // Vulnerable to physical damage
    ENERGY_VULNERABILITY,    // Vulnerable to energy weapons
    EXPLOSIVE_VULNERABILITY, // Vulnerable to explosions
    MELEE_VULNERABILITY,     // Vulnerable to melee attacks
    RANGED_VULNERABILITY,    // Vulnerable to ranged attacks
    FLANKING_VULNERABILITY,  // Vulnerable to flanking attacks
    HEIGHT_VULNERABILITY,    // Vulnerable when at lower height
    COVER_VULNERABILITY,     // Vulnerable when not in cover
    ISOLATION_VULNERABILITY, // Vulnerable when alone
    CROWDING_VULNERABILITY,  // Vulnerable when surrounded
    LIGHT_VULNERABILITY,     // Vulnerable in bright light
    DARKNESS_VULNERABILITY,  // Vulnerable in darkness
    NOISE_VULNERABILITY,     // Vulnerable to loud noises
    SILENCE_VULNERABILITY,   // Vulnerable in silence
    MOVEMENT_VULNERABILITY,  // Vulnerable while moving
    STATIONARY_VULNERABILITY, // Vulnerable when stationary
    ABILITY_VULNERABILITY,   // Vulnerable when using abilities
    WEAPON_VULNERABILITY,    // Vulnerable when using weapons
    ARMOR_VULNERABILITY,     // Vulnerable when wearing armor
    SHIELD_VULNERABILITY,    // Vulnerable when using shields
    HEALING_VULNERABILITY,   // Vulnerable while healing
    SUMMONING_VULNERABILITY, // Vulnerable while summoning
    TELEPORT_VULNERABILITY,  // Vulnerable while teleporting
    MIND_CONTROL_VULNERABILITY, // Vulnerable while mind controlling
    PSYCHIC_SENSITIVITY      // Sensitive to psychic attacks
}
