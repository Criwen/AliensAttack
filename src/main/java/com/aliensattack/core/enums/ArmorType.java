package com.aliensattack.core.enums;

/**
 * Defines different armor types with protection values
 */
public enum ArmorType {
    NONE,           // No armor
    LIGHT_ARMOR,    // Light armor (1-2 damage reduction)
    MEDIUM_ARMOR,   // Medium armor (2-4 damage reduction)
    HEAVY_ARMOR,    // Heavy armor (4-6 damage reduction)
    PLATED_ARMOR,   // Plated armor (6-8 damage reduction)
    POWERED_ARMOR   // Powered armor (8-10 damage reduction)
} 