package com.aliensattack.core.enums;

/**
 * Defines different types of Alien Reinforcements in XCOM 2
 */
public enum ReinforcementType {
    STANDARD,    // Standard reinforcement with basic units
    ESCALATING,  // Escalating reinforcement that gets stronger
    ADAPTIVE,    // Adaptive reinforcement that changes based on situation
    PERSISTENT,  // Persistent reinforcement that continues for multiple turns
    REPEATING    // Repeating reinforcement that spawns multiple times
}
