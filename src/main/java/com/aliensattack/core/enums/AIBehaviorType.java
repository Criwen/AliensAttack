package com.aliensattack.core.enums;

/**
 * Defines different AI behavior types for XCOM 2 aliens
 */
public enum AIBehaviorType {
    STANDARD,           // Standard tactical behavior
    AGGRESSIVE,         // Aggressive attack-focused behavior
    TACTICAL,           // Tactical positioning and coordination
    SUPPORT,            // Support and healing behavior
    BERSERKER,          // Berserker rage behavior
    CHARGE,             // Charge and melee behavior
    MOBILITY,           // High mobility and positioning
    AIR_SUPERIORITY,    // Air superiority and height advantage
    LEADERSHIP,         // Leadership and coordination
    DEFENSIVE,          // Defensive positioning
    STEALTH,            // Stealth and concealment
    SNIPER,             // Sniper and long-range behavior
    ASSAULT,            // Assault and close combat
    TECHNICAL,          // Technical and hacking behavior
    PSIONIC             // Psionic and mental abilities
}
