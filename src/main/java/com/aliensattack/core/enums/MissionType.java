package com.aliensattack.core.enums;

/**
 * Defines different mission types in XCOM 2
 */
public enum MissionType {
    ASSASSINATION,    // Kill specific target
    EXTRACTION,       // Extract VIP or item
    DEFENSE,          // Defend position or VIP
    SABOTAGE,         // Destroy or hack objective
    RECONNAISSANCE,   // Scout and gather intel
    ESCORT,           // Escort VIP to extraction
    HACKING,          // Hack terminals or systems
    DESTROY,          // Destroy alien facilities
    RESCUE,           // Rescue captured soldiers
    INFILTRATION,     // Stealth infiltration
    RETALIATION,      // Retaliation strike
    GUERRILLA,        // Guerrilla warfare
    SUPPLY_RAID,      // Raid supply convoy
    TERROR,           // Terror attack defense
    COUNCIL,          // Council mission
    ELIMINATION,      // Eliminate all enemies
    TIMED_ASSAULT,    // Timed assault mission
    DESTRUCTION,      // Destroy specific targets
    INTELLIGENCE,     // Gather intelligence
    CAPTURE,          // Capture targets
    INVASION          // Invasion defense
} 