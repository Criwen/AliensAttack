package com.aliensattack.core.enums;

/**
 * Defines different cover types
 */
public enum CoverType {
    NONE,           // No cover
    LIGHT,          // Light cover (+20% defense)
    HEAVY,          // Heavy cover (+40% defense)
    FULL,           // Full cover (+60% defense)
    FLANKED,        // Unit is flanked (no cover bonus)
    DEEP_COVER,     // Deep cover (+80% defense, +20% dodge)
    FULL_COVER,     // Full cover (+60% defense)
    HALF_COVER,     // Half cover (+40% defense)
    LOW_COVER       // Low cover (+20% defense)
} 