package com.aliensattack.core.enums;

/**
 * Defines different schools of psionic abilities
 * Each school provides unique bonuses and specializations
 */
public enum PsionicSchool {
    NONE,           // No psionic school specialization
    TELEPATHY,      // School of mental communication and information gathering
    MIND_CONTROL,   // School of controlling and influencing other minds
    PSYCHIC_WARFARE, // School of offensive psionic combat abilities
    TELEPORTATION,  // School of spatial manipulation and movement
    PSYCHIC_DEFENSE, // School of protective and defensive psionic abilities
    MIND_MERGE,     // School of consciousness sharing and unity
    PSYCHIC_MASTERY // School of general psionic power and versatility
}
