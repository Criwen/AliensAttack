package com.aliensattack.core.enums;

/**
 * Defines different types of Alien Pods in XCOM 2
 */
public enum AlienPodType {
    PATROL,     // Standard patrol pod with basic abilities
    AMBUSH,     // Ambush pod with surprise attack abilities
    DEFENSIVE,  // Defensive pod with protective abilities
    AGGRESSIVE  // Aggressive pod with offensive abilities
}
